alpha = "abdefg"

print(alpha[1:3])
print(alpha[3:])
print(alpha[:3])
print(alpha[3:-2])
print(alpha[-3:4])
print(alpha[::2])
print(alpha[1::2])


# slicing en Python es [start:end:step] 

# start es el índice de inicio para el slicing (incluido en la subsecuencia resultante).
# end es el índice de fin para el slicing (excluido de la subsecuencia resultante).
# step es el tamaño del paso o incremento para seleccionar elementos en la secuencia.