fahrenheit = 55 # Reemplazar con la temperatura que quieras
celsius = (fahrenheit - 32)/1.8 # Formula para el calculo y en la variable celsius se guarda los datos con todos los decimales.
celsius = round(celsius,2) #Aqui cogemos los datos de la variable y los redondeamos a dos decimales.
print (fahrenheit, "grados Fahrenheit son", celsius, "grados Celsius." ) # Hacemos un print del resultado.
